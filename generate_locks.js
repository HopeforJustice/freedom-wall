// Story lock positions in the middle area (avoiding outer 3 rows/columns of 32x32 grid)
// Grid positions 12,12 to 19,19 to ensure they're well within the middle
const storyLockIds = [396, 461, 525, 590, 654, 719, 783, 848, 912, 977];

function generateLockData() {
	const locks = [];

	for (let i = 0; i < 1024; i++) {
		const nameData = csvNames[i % csvNames.length];
		const isStoryLock = storyLockIds.includes(i);

		locks.push({
			name: nameData[0],
			date: nameData[1],
			id: i,
			story: isStoryLock,
		});
	}

	return locks;
}

const lockData = generateLockData();

// Generate the export string
let output = `// Lock data for each model
// Each entry contains name and date for a lock
// 1024 locks total for a 32x32 grid

export const lockData = [
`;

lockData.forEach((lock, index) => {
	output += `\t{\n`;
	output += `\t\tname: "${lock.name}",\n`;
	output += `\t\tdate: "${lock.date}",\n`;
	output += `\t\tid: ${lock.id},\n`;
	output += `\t\tstory: ${lock.story},\n`;
	output += `\t}${index < lockData.length - 1 ? "," : ""}\n`;
});

output += `];\n`;

